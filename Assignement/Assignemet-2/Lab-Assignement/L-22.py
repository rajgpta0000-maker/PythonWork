# Write a Python program to access values using dictionary keys. 
# Define a dictionary
my_dict = {"name": "Alice", "age": 30, "city": "New York"}
# Access values using dictionary keys
name = my_dict["name"]
age = my_dict["age"]
city = my_dict["city"]
# Print the accessed values
print("Name:", name)
print("Age:", age)
print("City:", city)
