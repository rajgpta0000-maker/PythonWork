# Create a list with elements of multiple data types
mixed_list = [1, "Hello", 3.14, True, None, [1, 2, 3], {"key": "value"}]
# Access elements at different index positions
print(mixed_list[0])  # Access the first element (integer)
print(mixed_list[1])  # Access the second element (string)
print(mixed_list[2])  # Access the third element (float)
print(mixed_list[3])  # Access the fourth element (boolean)
print(mixed_list[4])  # Access the fifth element (None)
print(mixed_list[5])  # Access the sixth element (list)
print(mixed_list[6])  # Access the seventh element (dictionary)
