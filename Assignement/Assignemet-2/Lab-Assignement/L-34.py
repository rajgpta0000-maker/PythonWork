#  Write a Python program to demonstrate the use of functions from the math module.
import math
# Get user input for a number
number = float(input("Enter a number: "))
# Calculate the square root, sine, and cosine of the number
sqrt_value = math.sqrt(number)
sine_value = math.sin(number)
cosine_value = math.cos(number)
# Print the results
print("The square root of the number is:", sqrt_value)
print("The sine of the number is:", sine_value)
print("The cosine of the number is:", cosine_value)


    