# Write a Python program to add elements to a list using insert() and append(). 
# Create an empty list
my_list = []
# Use append() to add elements to the end of the list
my_list.append(1)
my_list.append(2)
my_list.append(3)
# Use insert() to add an element at a specific index
my_list.insert(1, 10)  # This will insert 10 at index
# Print the final list
print(my_list)
