#  Write a Python program to separate keys and values from a dictionary using keys() and values() methods.
# Define a dictionary
my_dict = {"name": "Alice", "age": 30, "city": "New York"}
# Separate keys and values using keys() and values() methods
keys = my_dict.keys()
values = my_dict.values()
# Print the separated keys and values
print("Keys:", keys)
print("Values:", values)

