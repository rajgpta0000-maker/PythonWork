#  Q-27: Write a Python program to demonstrate string slicing
my_string = "Hello, World!"
print("Original string:", my_string)
print("Substring from index 1 to 5:", my_string[1:5])
print("Substring from index 0 to 5:", my_string[0:5])
print("Substring from index 7 to 12:", my_string[7:12])
print("Substring from index -6 to -1:", my_string[-6:-1])